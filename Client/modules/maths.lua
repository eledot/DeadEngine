local math = math;

function math.Clamp(n, min, max)
	return (n < min and min) or (n > max and max) or n;
end

function math.Distance(x1, y1, x2, y2)
	local x = x2 - x1;
	local y = y2 - y1;
	return math.sqrt(x*x + y*y);
end

return math;